    private void okBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okBtnActionPerformed
        
        int selectedRowCount = coursesTable.getSelectedRowCount();
        int[] selectedRows=new int[selectedRowCount];
        selectedRows=coursesTable.getSelectedRows();
        
        if(dtm.getRowCount()==0){
            
        }else if(selectedRowCount==0){
        
            WarningMessage.getInstance().showInterface("Select to add");
        }else{
       
        for (int i = 0; i < selectedRowCount; i++) {
        String course=dtm.getValueAt(selectedRows[i], 0).toString();
        String institute=dtm.getValueAt(selectedRows[i], 1).toString();
        String city=dtm.getValueAt(selectedRows[i], 2).toString();
            if(!NewApplication.getInstance().isEarlyAddedCourse(course, institute, city)){
            NewApplication.getInstance().addCourse(course, institute, city);
            
            }
        }
        this.dispose();
        }
        
    }//GEN-LAST:event_okBtnActionPerformed

