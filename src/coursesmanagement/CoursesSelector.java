    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        String searchWord=searchBoxTxt.getText();
        
        String query="SELECT inst.*,cour.* FROM institution inst INNER JOIN course cour ON inst.id="
                + "cour.institution_id WHERE (inst.city LIKE '"+searchWord+"%' OR inst.name LIKE '"+searchWord+"%'"
                + " OR cour.name LIKE '%"+searchWord+"%') AND inst.delete_status=0 AND cour.delete_status=0";
       retrieveData(query);
       
    }//GEN-LAST:event_searchBtnActionPerformed

