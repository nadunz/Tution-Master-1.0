    private void addCourse(String courseName,String institute,String city){
     
        dtm.addRow(new Object[]{courseName,institute,city});
    }
    
    public void retrieveData(){
      cleanTable();
      String query="SELECT inst.*,cour.* FROM institution inst INNER JOIN course cour ON inst.id="
                + "cour.institution_id WHERE inst.delete_status=0 AND cour.delete_status=0";
      Connect c=new Connect();
        ResultSet r=c.getQuery(query);
        try {
            while(r.next()){
           
            String courseName=r.getString("cour.name");
            String institute= r.getString("inst.name");
            String city=r.getString("inst.city");
                addCourse(courseName, institute, city);
                
            }
        } catch (SQLException ex) {
            Logger.getLogger(CoursesSelector.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
   
    private void retrieveData(String query){
      cleanTable();
      
      Connect c=new Connect();
        ResultSet r=c.getQuery(query);
        try {
            while(r.next()){
           
            String courseName=r.getString("cour.name");
            String institute=r.getString("inst.name");
            String city=r.getString("inst.city");
            
                
                addCourse(courseName, institute, city);
                
            }
        } catch (SQLException ex) {
            Logger.getLogger(CourseManager.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }

